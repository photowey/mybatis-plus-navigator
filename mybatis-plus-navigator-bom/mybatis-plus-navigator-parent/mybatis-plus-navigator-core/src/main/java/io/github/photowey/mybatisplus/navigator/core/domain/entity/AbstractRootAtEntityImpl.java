package io.github.photowey.mybatisplus.navigator.core.domain.entity;

import java.time.LocalDateTime;

/**
 * {@code AbstractRootAtEntityImpl}
 *
 * @author photowey
 * @version 3.5.5.1.0
 * @since 2024/07/20
 */
public abstract class AbstractRootAtEntityImpl extends AbstractCreatorEntityImpl implements RootAtEntity {

    protected LocalDateTime createdAt;
    protected LocalDateTime updatedAt;

    // ----------------------------------------------------------------

    @Override
    public LocalDateTime getCreatedAt() {
        return createdAt;
    }

    @Override
    public void setCreatedAt(LocalDateTime createdAt) {
        this.createdAt = createdAt;
    }

    @Override
    public LocalDateTime getUpdatedAt() {
        return updatedAt;
    }

    @Override
    public void setUpdatedAt(LocalDateTime updatedAt) {
        this.updatedAt = updatedAt;
    }
}